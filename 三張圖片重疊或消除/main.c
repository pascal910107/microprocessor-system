int stack[3] = {0};
int top = -1;
int isEmpty();
void push(int);
void pop();
void pressKey(int);
int inStack(int);
void render();

int main(void)
{
    int keyin;

    SYS_Init();

    init_LCD();
    clear_LCD();

    PD14 = 0;

    while (1)
    {
        keyin = ScanKey();

        switch (keyin)
        {
        case 1:
            pressKey(1);
            render();
            break;
        case 2:
            pressKey(2);
            render();
            break;
        case 3:
            pressKey(3);
            render();
            break;
        }

        CLK_SysTickDelay(50000);
    }
}

int isEmpty()
{
    if (top == -1)
    {
        return 1;
    }
    else
    {
        return 0;
    }
}

void push(int data)
{
    if (!(top >= 3))
    {
        top++;
        stack[top] = data;
    }
}

void pop()
{
    int data;
    if (!isEmpty())
    {
        data = stack[top];
        top--;
    }
}

int inStack(int data)
{
    int i;
    for (i = 0; i <= top; i++)
    {
        if (data == stack[i])
        {
            return 1;
        }
    }
    return 0;
}

void pressKey(int key)
{
    if (!inStack(key))
    {
        push(key);
    }
    else
    {
        if (stack[top] == key)
        {
            pop();
        }
    }
}

void render()
{
    int i;
    clear_LCD();
    CLK_SysTickDelay(20000);
    for (i = 0; i <= top; i++)
    {
        switch (stack[i])
        {
        case 1:
            draw_Bmp32x32(50, 30, FG_COLOR, BG_COLOR, one);
            break;
        case 2:
            draw_Bmp32x32(32, 30, FG_COLOR, BG_COLOR, two);
            break;
        case 3:
            draw_Bmp32x32(38, 12, FG_COLOR, BG_COLOR, three);
            break;
        }
        CLK_SysTickDelay(20000);
    }
}